export default function getTagColor(tag) {
  switch (tag) {
    case "Informática":
      return "tag-orange";
    case "Mecánica":
      return "tag-yellow";
    case "Electrónica":
      return "tag-blue";
    case "Química":
      return "tag-violet";
    case "Coordinador":
      return "tag-gray";
    case "Coordinadora":
      return "tag-gray";
  }
}
